package com.chat.model;

import java.sql.Timestamp;

public class Model_Group_File {
    private int id;
    private int messageId;
    private String filePath;
    private String fileType;
    private Timestamp uploadedAt;
    private String fileUrl; // URL để download file

    // Constructors
    public Model_Group_File() {
    }

    public Model_Group_File(int messageId, String filePath, String fileType) {
        this.messageId = messageId;
        this.filePath = filePath;
        this.fileType = fileType;
        this.uploadedAt = new Timestamp(System.currentTimeMillis());
    }

    // Getters and Setters
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getMessageId() {
        return messageId;
    }

    public void setMessageId(int messageId) {
        this.messageId = messageId;
    }

    public String getFilePath() {
        return filePath;
    }

    public void setFilePath(String filePath) {
        this.filePath = filePath;
    }

    public String getFileType() {
        return fileType;
    }

    public void setFileType(String fileType) {
        this.fileType = fileType;
    }

    public Timestamp getUploadedAt() {
        return uploadedAt;
    }

    public void setUploadedAt(Timestamp uploadedAt) {
        this.uploadedAt = uploadedAt;
    }

    public String getFileUrl() {
        return fileUrl;
    }

    public void setFileUrl(String fileUrl) {
        this.fileUrl = fileUrl;
    }
}